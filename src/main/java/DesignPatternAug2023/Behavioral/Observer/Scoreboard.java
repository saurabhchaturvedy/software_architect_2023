package DesignPatternAug2023.Behavioral.Observer;

public class Scoreboard {

    int runs;

    int wickets;

    public int getRuns() {
        return runs;
    }

    public void setRuns(int runs) {
        this.runs = runs;
    }

    public int getWickets() {
        return wickets;
    }

    public void setWickets(int wickets) {
        this.wickets = wickets;
    }
}
